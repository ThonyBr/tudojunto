// Sequência:
var n = 3;
n += 2;
window.alert(n); // Exibe: 5

// Condições (if/else)

// Exemplo com variável n:
var n = 30;

if (n >= 25) {
    // quando o if for true
    window.alert('O valor de n é MAIOR que 25!');
} else {
    // quando o if for false
    window.alert('O valor de n é MENOR que 25!');
}

// Condição simples:
n = 70;

if (n >= 200) {
    // true
    window.alert('O valor de n é MAIOR que 200!');
}

// Condição composta:
if (n >= 100) {
    // true
    window.alert('O valor de n é MAIOR que 100!');
} else {
    // false
    window.alert('O valor de n é MENOR que 100!');
}